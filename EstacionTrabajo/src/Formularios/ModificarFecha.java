    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Fabrica f = new Fabrica();
        IControladorFecha icF = f.getIControladorFecha();
        try {
            int anios = Integer.valueOf(a.getText());
            int mes = Integer.valueOf(m.getText());
            int dias = Integer.valueOf(d.getText());
            int horas = Integer.valueOf(h.getText());
            int min = Integer.valueOf(minutos.getText());
            int seg = Integer.valueOf(segundo.getText());

            DataFechaHora fecha = new DataFechaHora(dias,mes,anios,horas,min,seg);
            icF.setFecha(fecha);
            this.parent.setModoActivado(true);
            this.dispose();
            JOptionPane.showMessageDialog(null, "Se ha modificado correctamente la fecha a  " +  fecha.toString(), "Modificar Fecha", JOptionPane.INFORMATION_MESSAGE);        
        }catch (ExFechaInvalida e){
            JOptionPane.showInternalMessageDialog(this, "Datos Invalidos " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch (Exception e){
            JOptionPane.showInternalMessageDialog(this, "Datos Invalidos : "+ e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

