    public PartidosFinalizables(int idCom,Tarea1 parent) {
        initComponents();

        this.parent = parent;
        parent.setModoActivado(false);//no hace falta

        java.awt.Component content = parent.getContentPane();
        int x = (content.getWidth() - this.getWidth()) / 2;
        int y = (content.getHeight() - this.getHeight()) / 2;
        this.setLocation(x, y);

        aceptar.setEnabled(false);
        Fabrica f = new Fabrica();
        this.icCompeticiones = f.getIControladorCompeticiones();
        try {
            lista = icCompeticiones.listarPartidosConDivAsignado(idCom);
            String [] s = {"Fecha","Id Competencia","Nombre Competencia","Id Partido","Equipo Local","Equipo Visitante", "Lugar"};
            Object[][] tabla = new Object[lista.size()][7];
            for (int i =0;i< lista.size();i++) {
                DataInfoPartido d = lista.get(i);
                tabla[i][0]=  d.getFechaHora();
                tabla[i][1] = d.getIdComp();
                tabla[i][2] = d.getNomComp();
                tabla[i][3] = d.getIdPar();
                tabla[i][4] = d.getNomLocal();
                tabla[i][5] = d.getNomVisita();
                tabla[i][6] = d.getLugar();
            }
            TableModel dtm = new DefaultTableModel(tabla,s);
            TablaPartidosDivNoAsignados.setModel(dtm);
        }
        catch (Exception exc){
            javax.swing.JOptionPane.showMessageDialog(null, "Error: "+ exc.getMessage(),"Error",javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

