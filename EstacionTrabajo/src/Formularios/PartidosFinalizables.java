    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        // TODO add your handling code here:
        try{
           //Fabrica f = new Fabrica();
           //IControladorCompeticiones icc = f.getIControladorCompeticiones();
           int id = (Integer)this.TablaPartidosDivNoAsignados.getValueAt(fila, 3);
           DataInfoPartido dataIP = null;

           for (DataInfoPartido d:lista){
                if (d.getIdPar() == id){
                    dataIP = d;
               }
           }
           DataPartido dP=icCompeticiones.seleccionarPartido(dataIP);

           IControladorFecha icf = (new Fabrica()).getIControladorFecha();

         if(dP.getDataInfoPart().getFechaHora().compareTo(icf.getFecha())==1){
              JOptionPane.showMessageDialog(null, "La Fecha de Finalizado del Partido es Invalido","ERROR" ,JOptionPane.ERROR_MESSAGE);
              parent.setModoActivado(true);
              this.dispose();
          }else{

           javax.swing.JInternalFrame i = new IngresoResultado(dP,parent,icCompeticiones);
           parent.getLayeredPane().add(i);
           i.setVisible(true);
           this.dispose();
            }
        }
        catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, "Error :" +e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            parent.setModoActivado(true);
            this.dispose();
        }
    }//GEN-LAST:event_aceptarActionPerformed

    private void TablaPartidosDivNoAsignadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPartidosDivNoAsignadosMouseClicked
        // TODO add your handling code here:
        fila = this.TablaPartidosDivNoAsignados.rowAtPoint(evt.getPoint());
        columna = this.TablaPartidosDivNoAsignados.columnAtPoint(evt.getPoint());

        if ((fila > -1) && (columna > -1)){
            idComp = (Integer) this.TablaPartidosDivNoAsignados.getValueAt(fila,1);
            idPart = (Integer) this.TablaPartidosDivNoAsignados.getValueAt(fila,3);

            aceptar.setEnabled(true);
        }
    }//GEN-LAST:event_TablaPartidosDivNoAsignadosMouseClicked

