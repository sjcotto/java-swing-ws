    private void tablaCopasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCopasMouseClicked


        fila = tablaCopas.rowAtPoint(evt.getPoint());
        columna = tablaCopas.columnAtPoint(evt.getPoint());

        if ((fila > -1) && (columna > -1)){
            indiceSeleccionado  = (Integer) tablaCopas.getValueAt(fila,0);
            nombreCopa = (String) tablaCopas.getValueAt(fila,1);
            idCopa=(Integer)tablaCopas.getValueAt(fila,0);
            jToggleButton1.setEnabled(true);
        }


}//GEN-LAST:event_tablaCopasMouseClicked

