     private void cargarListaCopas(List<DataIdNombre> lista){
                String[] columnas = {"Id", "Nombre"};


                Iterator<DataIdNombre> it = lista.iterator();
                Object[][] data= new Object[lista.size()][2];

                for (int i = 0;i<lista.size();i++){
                     DataIdNombre c = (DataIdNombre) lista.get(i);
                     data[i][0] = c.getId();
                     data[i][1] = c.getNombre();
                }
                TableModel model = new DefaultTableModel(data,columnas);

                tablaCopas.setModel(model);
                final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(model);
                tablaCopas.setRowSorter(sorter);
                
                filtro.addKeyListener(new KeyListener() {
                    @Override
                    public void keyTyped(KeyEvent arg0) {
                        String i = "(?i)";
                        String texto = i.concat(filtro.getText());
                        if (texto.length() == 4) {
                            sorter.setRowFilter(null);
                        } else {
                            sorter.setRowFilter(RowFilter.regexFilter(texto));
                        }
                    }
                    @Override
                    public void keyReleased(KeyEvent arg0) {
                        String i = "(?i)";
                        String texto = i.concat(filtro.getText());
                        if (texto.length() == 4) {
                            sorter.setRowFilter(null);
                        } else {
                            sorter.setRowFilter(RowFilter.regexFilter(texto));
                        }
                    }
                    @Override
                    public void keyPressed(KeyEvent arg0) {
                        String i = "(?i)";
                        String texto = i.concat(filtro.getText());
                        if (texto.length() == 0) {
                                sorter.setRowFilter(null);
                            } else {
                                sorter.setRowFilter(RowFilter.regexFilter(texto));
                            }
                    }
           });

     }

